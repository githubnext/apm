// -------------------------------------------------------------------
// Config
// -------------------------------------------------------------------

// CompileTargetType names a supported output target.
type CompileTargetType = string

const (
	TargetAll       CompileTargetType = "all"
	TargetVSCode    CompileTargetType = "vscode"
	TargetAgents    CompileTargetType = "agents"
	TargetCopilot   CompileTargetType = "copilot"
	TargetClaude    CompileTargetType = "claude"
	TargetGemini    CompileTargetType = "gemini"
	TargetCursor    CompileTargetType = "cursor"
	TargetOpenCode  CompileTargetType = "opencode"
	TargetCodex     CompileTargetType = "codex"
	TargetWindsurf  CompileTargetType = "windsurf"
	TargetMinimal   CompileTargetType = "minimal"
	TargetAgentSkills CompileTargetType = "agent-skills"
)

// CompilationStrategy controls whether distributed or single-file output is used.
type CompilationStrategy = string

const (
	StrategyDistributed CompilationStrategy = "distributed"
	StrategySingleFile  CompilationStrategy = "single-file"
)

// BuildIDPlaceholder is replaced with the deterministic build ID after assembly.
const BuildIDPlaceholder = "<!-- APM_BUILD_ID_PLACEHOLDER -->"

// CopilotRootGeneratedMarker identifies files emitted by APM.
const CopilotRootGeneratedMarker = "<!-- Generated by APM CLI from .apm/ primitives -->"

// CompilationConfig holds all options for a compilation run.
type CompilationConfig struct {
	OutputPath    string
	Chatmode      string
	ResolveLinks  bool
	DryRun        bool
	WithConstitution bool
	Target        CompileTargetType
	Strategy      CompilationStrategy
	SingleAgents  bool
	Verbose       bool
	Quiet         bool
	WorkDir       string
}

// DefaultConfig returns sensible defaults.
func DefaultConfig() CompilationConfig {
	return CompilationConfig{
		OutputPath:       "AGENTS.md",
		ResolveLinks:     true,
		WithConstitution: true,
		Target:           TargetAll,
		Strategy:         StrategyDistributed,
	}
}

// -------------------------------------------------------------------
// Results
// -------------------------------------------------------------------

// CompilationResult captures the outcome of one target's compilation.
type CompilationResult struct {
	Target     CompileTargetType
	OutputPath string
	Content    string
	BuildID    string
	LinesOut   int
	ElapsedMS  int64
	Error      error
	Warnings   []string
	DryRun     bool
}

// OK returns true when the result has no error.
func (r CompilationResult) OK() bool { return r.Error == nil }

// MergedResult summarises a multi-target compilation.
type MergedResult struct {
	Results  []CompilationResult
	Errors   []error
	Warnings []string
	TotalMS  int64
}

// OK returns true when all results are OK.
func (m MergedResult) OK() bool {
	for _, r := range m.Results {
		if r.Error != nil {
			return false
		}
	}
	return true
}

// -------------------------------------------------------------------
// AgentsCompiler
